package ex14;

public class Student2 {
	private String name;
	private String sex;
	private int eng;
	
	public Student2(String name, String sex, int eng) {
		this.name = name;
		this.sex = sex;
		this.eng = eng;
	}

	public String getName() {
		return name;
	}

	public int getEng() {
		return eng;
	}

	public String getSex() {
		return sex;
	}
	
}
